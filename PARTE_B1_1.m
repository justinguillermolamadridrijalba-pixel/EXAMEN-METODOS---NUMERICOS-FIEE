%PARTE B (INTERPOLACION)%
%Use interpolación polinómica de grado adecuado sobre todo el rango con método Matricial e interpolación de Lagrange%

%METODO MATRICIAL%

f = [100, 120, 145, 170, 200, 235, 270, 310, 355, 405, 460, 520, 585, 655, 730, 810, 895, 985, 1080, 1180, 1290, 1410, 1540, 1680, 1830, 1990, 2160, 2340, 2530, 2730];

z = [152.3, 149.1, 146.8, 144.9, 142.0, 139.5, 137.9, 136.1, 134.8, 133.6, 132.7, 131.9, 131.4, 131.1, 130.9, 131.0, 131.3, 131.9, 132.7, 133.8, 135.2, 136.9, 138.9, 141.1, 143.5, 146.1, 149.0, 152.2, 155.6, 159.2];

n=length(f);

%CREAMOS LA MATRIZ V COMO UN ESQUEMA VACIO QUE SE LLENA CON LOS COMPONENTES DE LA MATRIZ DE COEFICIENTE%
V=zeros(n, n);   

%CREAMOS LA MATRIZ DE COEFICIENTES PARA PODER OPERAR%
for i = 1:n
    for j = 1:n
        %EN ESTA LINEA LLENAMOS LA MATRIZ V, CON LOS COEFICIENTES SEGUN LA INTERPOLACION MATRICIAL%
        V(i, j) = f(i)^(j-1);
    end
end

%CAMBIAMOS LA ORIENTACION DE LA COLUMNA Z PARA PODER MULTIPLICAR (TRANSPONEMOS LA MATRIZ)%

z_TRANSPUESTA = z';

%RESOLVEMOS EL SISTEMA DE ECUACIONES SEGUN LA ECUACION MATRICIAL A = INVERSA DE V * Z_TRANSPUESTA %

INVERA_V = inv(V);
A = INVERA_V * z_TRANSPUESTA;

%MUESTRA DE RESULTADOS%

disp('LOS VALORES SON');
disp(A(1:30)); 
